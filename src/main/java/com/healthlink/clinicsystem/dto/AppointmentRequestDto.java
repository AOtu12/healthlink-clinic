package com.healthlink.clinicsystem.dto;

import java.time.LocalDate;
import java.time.LocalTime;

public class AppointmentRequestDto {

    private Long patientId;          // Patient making appointment
    private Long doctorId;           // Assigned doctor
    private LocalDate appointmentDate; // Date selected
    private LocalTime appointmentTime; // Time selected
    private String notes;            // Optional notes

    public AppointmentRequestDto() {
    }

    public Long getPatientId() {
        return patientId;
    }

    public void setPatientId(Long patientId) {
        this.patientId = patientId;
    }

    public Long getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(Long doctorId) {
        this.doctorId = doctorId;
    }

    public LocalDate getAppointmentDate() {
        return appointmentDate;
    }

    public void setAppointmentDate(LocalDate appointmentDate) {
        this.appointmentDate = appointmentDate;
    }

    public LocalTime getAppointmentTime() {
        return appointmentTime;
    }

    public void setAppointmentTime(LocalTime appointmentTime) {
        this.appointmentTime = appointmentTime;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }
}
